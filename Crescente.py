#Estrutura de repetição for de teste
for n in range(10):
    print(n)

#Com o for posso passar a posição inicial como primeiro valor no parametro do range e até onde ira contar no segundo valor do parametro
for n in range(4, 20):
    print(n)

#For com ordem decrescente, o primeiro valor é onde inicia, o segundo onde ira parar e o valor a decrementar (negativo)
for n in range(10, 0, -1):
    print(n)

#Estrutura de repetição com while
x = 1
while x <= 15:
    print(x);
    x = x + 1